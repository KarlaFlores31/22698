package com.example.a22698.matematicas

fun Score(
    uno: Int,
    dos: Int,
    tres: Int,
    cuatro: Int,
    cinco: Int,
    seis: Int,
    siete: Int,
    ocho: Int,
    nueve: Int,
    diez: Int,
    once: Int,
    doce: Int,
    trece: Int,
    catorce: Int,
    quince: Int,
    dieciseis: Int
): Int {
    return uno + dos + tres + cuatro + cinco + seis +
            siete + ocho + nueve + diez + once + doce +
            trece + catorce + quince + dieciseis
}
